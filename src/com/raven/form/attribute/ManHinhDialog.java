    private void btnSuaManHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaManHinhActionPerformed
        // TODO add your handling code here:
        int row = tableManHinh.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (ManHinhResponse dl : service.getAll()) {
                if (!dl.getLoaiManHinh().equals(txtTenMh.getText()) && String.valueOf(dl.getId()).equals(txtMaMh.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên màn hình này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableManHinh.getValueAt(row, 0);
            ManHinhResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showData(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaManHinhActionPerformed
    ManHinhResponse getForm() {
        String ten = txtTenMh.getText();
        return new ManHinhResponse(ten);
    }

