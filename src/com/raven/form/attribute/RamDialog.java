    private void btnSuaRamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaRamActionPerformed
        // TODO add your handling code here:
        int row = tableRam.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (RamResponse dl : service.getAll()) {
                if (!dl.getDungLuongRam().equals(txtTenRm.getText()) && String.valueOf(dl.getId()).equals(txtMaRm.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên ram này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableRam.getValueAt(row, 0);
            RamResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showDataRam(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaRamActionPerformed

