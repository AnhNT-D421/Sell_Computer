    private void btnThemManHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemManHinhActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenMh.getText();
            if (isManHinhExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                ManHinhResponse mhr = getForm();
                if (service.add(mhr)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showData(service.getAll());
                    product.showManHinh(service.getLoaiManHinh());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }

            }
        }
    }//GEN-LAST:event_btnThemManHinhActionPerformed
    public Boolean isManHinhExisted(String tenThuocTinh) {
        for (ManHinhResponse exmh : service.getLoaiManHinh()) {
            if (tenThuocTinh.equals(exmh.getLoaiManHinh())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenMh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên màn hình");
            return false;
        }
        if (!txtTenMh.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝ]+")) {
            JOptionPane.showMessageDialog(this, "Tên màn hình chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
