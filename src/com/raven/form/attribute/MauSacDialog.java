    private void btnSuaMSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaMSActionPerformed
        // TODO add your handling code here:
        int row = tableMauSac.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (MauSacResponse dl : service.getAll()) {
                if (!dl.getTenMauSac().equals(txtTenMs.getText()) && String.valueOf(dl.getId()).equals(txtMaMs.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên màu này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableMauSac.getValueAt(row, 0);
            MauSacResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showData(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaMSActionPerformed

