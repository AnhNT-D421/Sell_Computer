    private void btnThemDLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDLActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenDl.getText();
            if (isDungLuongExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                DungLuongResponse clr = getForm();
                if (service.add(clr)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showData(service.getAll());
                    product.showDungLuong(service.getDungLuongS());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }

            }
        }
    }//GEN-LAST:event_btnThemDLActionPerformed
    public Boolean isDungLuongExisted(String tenThuocTinh) {
        for (DungLuongResponse exdl : service.getDungLuongS()) {
            if (tenThuocTinh.equals(exdl.getDungLuong())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenDl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên dung lượng");
            return false;
        }
        if (!txtTenDl.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝ]+")) {
            JOptionPane.showMessageDialog(this, "Tên dung lượng chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
