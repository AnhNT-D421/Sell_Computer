    private void btnThemNSXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNSXActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenNsx.getText();
            if (isNhaSanXuatExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            NhaSanXuatResponse nsxr = getForm();
            if (service.add(nsxr)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                showDataNSX(service.getAll());
                product.showNhaSanXuat(service.getTenNhaSanXuat());
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
        }
    }//GEN-LAST:event_btnThemNSXActionPerformed
    public Boolean isNhaSanXuatExisted(String tenThuocTinh) {
        for (NhaSanXuatResponse exnsx : service.getTenNhaSanXuat()) {
            if (tenThuocTinh.equals(exnsx.getTenNhaSanXuat())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenNsx.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên nhà sản xuất");
            return false;
        }
        if (!txtTenNsx.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝ]+")) {
            JOptionPane.showMessageDialog(this, "Tên nhà sản xuất chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
