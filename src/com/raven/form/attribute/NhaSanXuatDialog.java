    private void btnSuaNSXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaNSXActionPerformed
        // TODO add your handling code here:
        int row = tableNhaSanXuat.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (NhaSanXuatResponse dl : service.getAll()) {
                if (!dl.getTenNhaSanXuat().equals(txtTenNsx.getText()) && String.valueOf(dl.getId()).equals(txtMaNsx.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên nhà sản xuất này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableNhaSanXuat.getValueAt(row, 0);
            NhaSanXuatResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showDataNSX(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaNSXActionPerformed

