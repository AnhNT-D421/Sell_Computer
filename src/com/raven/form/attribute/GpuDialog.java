    private void btnThemGpuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemGpuActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenGu.getText();
            if (isGpuExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                GpuResponse gpu = getForm();
                if (service.add(gpu)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showData(service.getAll());
                    product.showGpu(service.getGpu());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnThemGpuActionPerformed
    public Boolean isGpuExisted(String tenThuocTinh) {
        for (GpuResponse exgpu : service.getGpu()) {
            if (tenThuocTinh.equals(exgpu.getLoaiGPU())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenGu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên gpu");
            return false;
        }
        if (!txtTenGu.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝ]+")) {
            JOptionPane.showMessageDialog(this, "Tên gpu chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
