    private void btnThemMSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMSActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenMs.getText();
            if (isMauSacExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                MauSacResponse ms = getForm();
                if (service.add(ms)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showData(service.getAll());
                    product.showMauSac(service.getTenMauSac());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnThemMSActionPerformed
    public Boolean isMauSacExisted(String tenThuocTinh) {
        for (MauSacResponse exms : service.getTenMauSac()) {
            if (tenThuocTinh.equals(exms.getTenMauSac())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenMs.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên màn hình");
            return false;
        }
        if (!txtTenMs.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝiíìịỉĩIÍỈỊÌ]+")) {
            JOptionPane.showMessageDialog(this, "Tên màn hình chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
