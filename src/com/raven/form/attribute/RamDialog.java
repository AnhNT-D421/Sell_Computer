    private void btnThemRamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemRamActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenRm.getText();
            if (isRamExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                RamResponse gpu = getForm();
                if (service.add(gpu)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showDataRam(service.getAll());
                    product.showRam(service.getDungLuongRam());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnThemRamActionPerformed
    public Boolean isRamExisted(String tenThuocTinh) {
        for (RamResponse exrm : service.getDungLuongRam()) {
            if (tenThuocTinh.equals(exrm.getDungLuongRam())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenRm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên ram");
            return false;
        }

        String regex = "^[0-9]+$";
        if (!txtTenRm.getText().matches(regex)) {
            JOptionPane.showMessageDialog(this, "Tên ram chỉ được chứa số");
            return false;
        }

        return true;
    }

