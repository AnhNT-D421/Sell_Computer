    private void btnThemCpuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemCpuActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String ten = txtTenCu.getText();
            if (isCpuExisted(ten)) {
                JOptionPane.showMessageDialog(this, "Tên thuộc tính đã tồn tại");
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không ?") == 0) {
                CpuResponse clr = getForm();
                if (service.add(clr)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showData(service.getAll());
                    product.showCpu(service.getLoaiCpu());
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnThemCpuActionPerformed
    public Boolean isCpuExisted(String tenThuocTinh) {
        for (CpuResponse excpu : service.getLoaiCpu()) {
            if (tenThuocTinh.equals(excpu.getLoaiCPU())) {
                return true;
            }
        }
        return false;
    }

    boolean validateForm() {
        if (txtTenCu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn cần nhập tên cpu");
            return false;
        }
        if (!txtTenCu.getText().matches("[a-zA-Z0-9\\sàáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễđòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳỹỷýÀÁẠẢÃÂẦẤẬẨẪĂẰẮẶẲẴÈÉẸẺẼÊỀẾỆỂỄĐÒÓỌỎÕÔỒỐỘỔỖƠỜỚỢỞỠÙÚỤỦŨƯỪỨỰỬỮỲỸỶÝ]+")) {
            JOptionPane.showMessageDialog(this, "Tên cpu chỉ được chứa số, chữ và khoảng trắng");
            return false;
        }
        return true;
    }
