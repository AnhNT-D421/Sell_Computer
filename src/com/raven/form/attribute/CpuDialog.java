    private void btnSuaCpuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaCpuActionPerformed
        // TODO add your handling code here:
        int row = tableCpu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (CpuResponse cl : service.getAll()) {
                if (!cl.getLoaiCPU().equals(txtTenCu.getText()) && String.valueOf(cl.getId()).equals(txtMaCu.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên cpu này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableCpu.getValueAt(row, 0);
            CpuResponse cpu = getForm();
            if (service.update(cpu, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showData(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaCpuActionPerformed

