    private void btnSuaDLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDLActionPerformed
        // TODO add your handling code here:
        int row = tableDungLuong.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (DungLuongResponse dl : service.getAll()) {
                if (!dl.getDungLuong().equals(txtTenDl.getText()) && String.valueOf(dl.getId()).equals(txtMaDl.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên dung lượng này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableDungLuong.getValueAt(row, 0);
            DungLuongResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showData(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaDLActionPerformed

