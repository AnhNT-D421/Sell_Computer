    private void btnSuaGpuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaGpuActionPerformed
        // TODO add your handling code here:
        int row = tableGpu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 dòng");
            return;
        }
        if (validateForm()) {
            for (GpuResponse dl : service.getAll()) {
                if (!dl.getLoaiGPU().equals(txtTenGu.getText()) && String.valueOf(dl.getId()).equals(txtMaGu.getText())) {
                    JOptionPane.showMessageDialog(this, "Tên gpu này đã tồn tại bạn có thể sử dụng lại");
                    return;
                }
            }
            int ma = (int) tableGpu.getValueAt(row, 0);
            GpuResponse kh = getForm();
            if (service.update(kh, ma)) {
                JOptionPane.showMessageDialog(this, "Thành công");
                showData(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaGpuActionPerformed

